// Welcome to JavaScript!
// Let's learn about varibles and data types

// String variables
let myName = "Josh";
let greeting = "Hello, World!";
let favoriteColor = "Blue";

// Number variables 
let age = 20;
let price = 99.99;

// Boolean variables
let isLearning = true;
let isComplete = false;

// Let's see what we created!

console.log("=== My First JavaScript Program ===");
console.log("My name is:", myName);
console.log("Greeting:", greeting);
console.log("Age:", age);
console.log("Price: $" + price);
console.log("Am I learning?", isLearning);
console.log("Is lesson complete?", isComplete);
console.log("My favorite color:", favoriteColor);

// Let's check the data types using typeof
console.log("\n=== Data Types ===");
console.log("typeof myName:", typeof myName);
console.log("typeof age:", typeof age);
console.log("typeof isLearning:", typeof isLearning);
console.log("typeof favoriteColor:", typeof favoriteColor);



